package apackage.com.yournight.view.Adapter;

public class EventListModel {


    private int eventID;

    private String eventName;

    private String eventPlace;

    private String eventDate;

    private String eventStartime;

    private  int eventMemberAmount;

    private String eventPicturePath;


    public int getEventID() {
        return eventID;
    }

    public void setEventID(int eventID) {
        this.eventID = eventID;
    }

    public String getEventName() {
        return eventName;
    }

    public void setEventName(String eventName) {
        this.eventName = eventName;
    }

    public String getEventPlace() {
        return eventPlace;
    }

    public void setEventPlace(String eventPlace) {
        this.eventPlace = eventPlace;
    }

    public String getEventDate() {
        return eventDate;
    }

    public void setEventDate(String eventDate) {
        this.eventDate = eventDate;
    }

    public String getEventStartime() {
        return eventStartime;
    }

    public void setEventStartime(String eventStartime) {
        this.eventStartime = eventStartime;
    }

    public int getEventMemberAmount() {
        return eventMemberAmount;
    }

    public void setEventMemberAmount(int eventMemberAmount) {
        this.eventMemberAmount = eventMemberAmount;
    }

    public String getEventPicturePath() {
        return eventPicturePath;
    }

    public void setEventPicturePath(String eventPicturePath) {
        this.eventPicturePath = eventPicturePath;
    }



}
